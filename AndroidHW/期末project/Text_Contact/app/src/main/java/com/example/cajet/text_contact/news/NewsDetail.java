package com.example.cajet.text_contact.news;


class NewsDetail {

    private String title;
    private String info;
    private String texts;

    String getTitle() {
        return title;
    }

    void setTitle(String title) {
        this.title = title;
    }

    String getInfo() {
        return info;
    }

    void setInfo(String info) {
        this.info = info;
    }

    String getTexts() {
        return texts;
    }

    void setTexts(String texts) {
        this.texts = texts;
    }
}
